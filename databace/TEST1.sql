CREATE TABLE EMP01 AS 
SELECT E.*, SAL+100 AS SAL1 
FROM EMP E;

--EMP01테이블에서 변경
--SAL1 컬럼의 값을 모두 0으로 수정
UPDATE EMP01
SET SAL1 = 0;
COMMIT;

/*
EMP01테이블에서 변경
SAL01 컬럼의 값을 연봉의 10퍼 인상값으로 수정
*/
UPDATE EMP01
SET SAL1 = SAL*1.1;
ROLLBACK;

/*
급여가 3000이상인 사원만 급여 10퍼 인상 그 값을 SAL1에 수정
*/
UPDATE EMP01
SET SAL1 = SAL * 1.1
WHERE SAL >= 3000;
ROLLBACK;

/*
