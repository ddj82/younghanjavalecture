package school_05;

public class A01_Memory {
    String str;
    int num;

    A01_Memory() {}
    public A01_Memory(String str, int num) {
        this.str = str;
        this.num = num;
    }
    public String returnStr() {
        return str;
    }

    public int returnNum() {
        return num;
    }
}
