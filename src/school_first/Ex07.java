package school_first;

public class Ex07 {
    public static void main(String[] args) {
        int a = 10, b = 5;

        System.out.println(a == b);
        System.out.println(!(a == b));
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a < b);
        System.out.println(a <= b);
        System.out.println(a != b);
    }
}
